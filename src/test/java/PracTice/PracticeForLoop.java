package PracTice;

public class PracticeForLoop {

    public static void main(String[] args) {

        //for loop = executes a block of code for a limited amount times
        for(int i=10; i>0; i--){
            System.out.println("my kids age are :" +i);
        }//end of for loop
    }//end of method
}//end of class
